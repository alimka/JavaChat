    public void showMessage(Message msg) {
        String txt = null;
        if (msg.getTo() != null) {
            txt = msg.getFrom() + " -> " + msg.getTo() + ": " + msg.getMessage() + "\n";
            colorTextPane.append(Color.blue, txt);
        } else {
            txt = msg.getFrom() + ": " + msg.getMessage() + "\n";
            colorTextPane.append(Color.black, txt);
        }

    }

